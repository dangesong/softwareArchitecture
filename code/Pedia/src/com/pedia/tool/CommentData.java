package com.pedia.tool;

/*
 * 评论的详细数据
 */
public class CommentData {
	private String commenterPic;
	private String commenterName;
	private String commentDate;
	private String commentDetail;
	
	public CommentData(){
		
	}


	public String getCommenterPic() {
		return commenterPic;
	}


	public void setCommenterPic(String commenterPic) {
		this.commenterPic = commenterPic;
	}


	public String getCommenterName() {
		return commenterName;
	}

	public void setCommenterName(String commentName) {
		this.commenterName = commentName;
	}

	public String getCommentDate() {
		return commentDate;
	}

	public void setCommentDate(String commentDate) {
		this.commentDate = commentDate;
	}

	public String getCommentDetail() {
		return commentDetail;
	}

	public void setCommentDetail(String commentDetail) {
		this.commentDetail = commentDetail;
	}
	
	
	
	
}

